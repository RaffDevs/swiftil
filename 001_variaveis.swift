import Foundation

func variaveis() {
    // MARK: --------------------------------------------- Variaveis

    // String
    var name: String = "Rafael"
    print("String", name)

    // Int
    var age: Int = 12
    print("Int", age)

    // Bool
    var isActive: Bool = true
    print("Boolean", isActive)

    // Float (Menos preciso nas casas decimais)
    var fraction: Float = 1.15199529295291291915915915
    print("Float", fraction)

    // Double (Mais preciso nas casas decimais)
    var anotherFraction: Double = 1.15199529295291291915915915
    print("Double", anotherFraction)

}
